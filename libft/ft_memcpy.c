#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	size_t			i;
	unsigned char	*str;
	unsigned char	*res;

	i = 0;
	str = (unsigned char *)src;
	res = (unsigned char *)dst;
	while (i < n)
	{
		*res++ = *str++;
		i++;
	}
	return (dst);
}
